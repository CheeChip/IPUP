#++++++++++++++++++++++++++++++++++++++++++++++++
# 题目
#   （几何方面：正六边形的面积）编写一个程序，提示用户输入
#   正六边形的边长并且显示它的面积。正六边形面积的计算公式是 
#       a = \frac {3 \sqrt{3}} {2} * s^2
#++++++++++++++++++++++++++++++++++++++++++++++++

s = eval(input('Enter the side: '))
k = 0.5 * 3 * 3 ** 0.5
area = k * s ** 2
area = round(area, 4)
print('The area of the hexagon is', area)