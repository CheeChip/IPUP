# 题目
#   （对一个整数中的各位数字求和）编写一个程序，读取一个 0 到
#   1000 之间的整数并计算它各位数字之和。
#   例如：输入一个整数是 932，那么它各位数字之和是 14。
#++++++++++++++++++++++++++++++++++++++++++++++++

num = int(input('Enter a number between 0 and 1000: '))
s = 0
while num > 0:
    s += num % 10
    num //= 10
print('The sum of the digits is', s)