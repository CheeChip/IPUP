# 题目
#   （金融应用程序：计算利息）如果你知道差额和百分比的
#   年利率，你可以使用下面的公式计算下个月的月供利息
#       利息 = 差额 * (年利率 / 1200 )
#   编写一个读取差额和年利率，然后显示下月要付利息的程序。
#++++++++++++++++++++++++++++++++++++++++++++++++

balance, rate = eval(input(
    "Enter balance and interest rate (e.g., 3 for 3%): "))

interest = balance * rate / 1200

print("The interest is %4f" % (interest))