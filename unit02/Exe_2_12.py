# 题目
#   （打印表格）编写一个显示下面的表格的程序
#       a       b       a ** b
#       1       2       1
#       2       3       8
#       3       4       81
#       4       5       1024
#       5       6       15625
#++++++++++++++++++++++++++++++++++++++++++++++++
print("a\tb\ta ** b")

for i in range(1, 6):
    print(i, '\t', i+1, '\t', i**(i+1), sep='')